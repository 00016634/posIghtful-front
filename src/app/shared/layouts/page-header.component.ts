import { Component, Input } from '@angular/core';
import { NgClass, NgIf } from '@angular/common';
import { Router } from '@angular/router';

@Component({
  selector: 'app-page-header',
  standalone: true,
  imports: [NgClass, NgIf],
  template: `
    <div [ngClass]="['flex items-center justify-between mb-6', className]">
      <div class="flex items-center gap-4">
        <button
          *ngIf="backRoute"
          class="inline-flex items-center justify-center rounded-md text-sm font-medium h-9 w-9 border border-input bg-background shadow-sm hover:bg-accent hover:text-accent-foreground"
          (click)="goBack()"
        >
          <svg xmlns="http://www.w3.org/2000/svg" width="16" height="16" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round"><path d="m12 19-7-7 7-7"/><path d="M19 12H5"/></svg>
        </button>
        <div>
          <h1 class="text-2xl font-bold tracking-tight">{{ title }}</h1>
          <p *ngIf="subtitle" class="text-muted-foreground">{{ subtitle }}</p>
        </div>
      </div>
      <div class="flex items-center gap-2">
        <ng-content />
      </div>
    </div>
  `,
})
export class PageHeaderComponent {
  @Input() title = '';
  @Input() subtitle = '';
  @Input() backRoute = '';
  @Input() className = '';

  constructor(private router: Router) {}

  goBack() {
    this.router.navigateByUrl(this.backRoute);
  }
}
